c-----------------------------------------------------------------------
c
c This module stores file IDs that are generated by HDF5. For the HDF5
c based I/O we will use 2 files:
c - fid_dat: for general data that the program generates
c - fid_rst: for restart data (that may be discarded when the
c            calculation finishes
c HDF5 uses special data types for their file IDs. If the code is
c compiled without HDF5 we will use standard integers instead. This 
c way named conflicts can still be detected.
c
      module files_mod
#ifdef USE_HDF5
        use hdf5
        integer(hid_t) :: fid_dat
        integer(hid_t) :: fid_rst
#else
        integer, parameter :: fid_dat = -1
        integer, parameter :: fid_rst = -1
#endif
      end module files_mod
c
c-----------------------------------------------------------------------
